        ...array_filter(
            explode(',', env('APP_PREVIOUS_KEYS', ''))
        ),
    ],

    /*
    |--------------------------------------------------------------------------
    | メンテナンスモードのドライバー
    |--------------------------------------------------------------------------
    |
    | Laravelのメンテナンスモードを制御するための方法を指定します。
    | "file" または "cache" を選択可能です。
    | "cache" を使うと複数サーバー間でも共有管理が可能になります。
    |
    */
    'maintenance' => [
        'driver' => env('APP_MAINTENANCE_DRIVER', 'file'),
        'store' => env('APP_MAINTENANCE_STORE', 'database'),
    ],

];
